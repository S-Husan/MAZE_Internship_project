def print_binary_matrix(matrix):
    print("--- RAW 0 AND 1 MATRIX ---")
    for row in matrix:
        print(" ".join(str(cell) for cell in row))
    print("\n")

def print_visual_maze(matrix):
    print("--- VISUAL TERMINAL MAZE ---")
    print("Entrance is at the bottom right (↑), Exit is at the top left (←).\n")
    for r, row in enumerate(matrix):
        row_str = ""
        for c, cell in enumerate(row):
            if cell == 1:
                row_str += "█"  # Wall
            elif r == 15 and c == 16:
                row_str += "↑"  # Entrance arrow
            elif r == 1 and c == 0:
                row_str += "←"  # Exit arrow
            else:
                row_str += "·"  # Path dots from your drawing
        print(row_str)

